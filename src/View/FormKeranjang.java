    public void judul() {
        
        Object[] judul = {
            "Kode Barang", "Nama Barang", "Harga", "Ukuran", "Jumlah", "Subtotal", "Total", "Bayar", "Kembalian", "Tanggal"
        };
        tableModel = new DefaultTableModel(null, judul);
        tb_keranjang.setModel(tableModel);
    }
  
    public void tampilData(String where) {
        
        try {
            st = cn.createStatement();
            tableModel.getDataVector().removeAllElements();
            tableModel.fireTableDataChanged();
            rs = st.executeQuery("SELECT * FROM transaksi " + where);
            
            while (rs.next()) {
                Object[] data = {
                    rs.getString("kode_barang"),
                    rs.getString("nama_barang"),
                    rs.getString("harga"),
                    rs.getString("ukuran"),
                    rs.getString("jumlah"),
                    rs.getString("subtotal"),
                    rs.getString("total"),
                    rs.getString("bayar"),
                    rs.getString("kembalian"),
                    rs.getString("tanggal"),
                };
                tableModel.addRow(data);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    public void total(){
        int total = 0;
        for (int i = 0; i < tb_keranjang.getRowCount(); i++){
            int amount = Integer.parseInt((String)tb_keranjang.getValueAt(i, 5));
            total += amount;
        }
        txtTotal.setText("" + total);
    }
    
    public void bayar(){
        
        int total = Integer.parseInt(txtTotal.getText());
        int bayar = Integer.parseInt(txtBayar.getText());

        if (bayar < total) {
            JOptionPane.showMessageDialog(null, "Pembayaran harus lebih dari total harga!");
            txtBayar.setText("");
        } else {
            int change = bayar - total;
            txtKembalian.setText(Integer.toString(change));
        }
    }
    
    public void tanggal(){
        Date currentDate = new Date();
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");

        // Mengisi nilai tanggal ke dalam JTextField dengan nama txtDate
        txtTanggal.setText(dateFormat.format(currentDate));
    }
    
