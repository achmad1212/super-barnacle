    private void bt_insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_insertActionPerformed
        try {
            st = cn.createStatement();
            String kode_barang = txtKode.getText();
            String nama_barang = txtMenu.getText();
            String harga = txtHarga.getText();
            String ukuran = txtUkuran.getText();
            String jumlah = txtJumlah.getText();
            String subtotal = txtSubtotal.getText();
            String total = txtTotal.getText();
            String bayar = txtBayar.getText();
            String kembalian = txtKembalian.getText();
            String tanggal = txtTanggal.getText();

            String updateQuery = "UPDATE transaksi SET "
            + "total='" + total + "', "
            + "bayar='" + bayar + "', "
            + "tanggal='" + tanggal + "', "
            + "kembalian='" + kembalian + "'WHERE kode_barang='" + kode_barang + "'";

            st.executeUpdate(updateQuery);

            tampilData("");

        } catch (Exception e) {
            e.printStackTrace();
        }
        bt_update.setEnabled(false);
        bt_delete.setEnabled(false);
        bt_back.setEnabled(true);
        bt_print.setEnabled(true);
    }//GEN-LAST:event_bt_insertActionPerformed

